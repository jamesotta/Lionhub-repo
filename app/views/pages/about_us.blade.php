							The application stage is a 3 – 12 weeks period by which interested applicants are expected to quickly submit their applications in order to gain admission into the Hub. During this stage, they are expected to answer questions in their application form, submit a business plan and subsequently take a physical interview which will help analyze their readiness and ultimately determine if they will be admitted into the program or not.
						</p>

						<p class="bm-remove">
							The first three weeks opens up the application website, http://lionhub.unn.edu.ng/apply. Here the team which most consist of 2 – 5 persons is expected to answer questions about their business idea, their current skillset and their team. After applications close, the ICT and Business Coordinators of LionHUB will each perform their own separate analysis directly on the website during a 3 week period. After which the final results are automatically calculated and up to 50 are published for the second stage. During the second stage, the shortlisted applicants have 2 weeks to submit their full business plans containing their executive summary.
						</p>
                        <p class="bm-remove">
                         The review process begins again and after 2 weeks, 30 participants will be shortlisted for interview and required to appear for physical interview shared among the coordinators in a two week period, after which the final results are published. Up to 20 teams and maximum of 50 students will be admitted at the final stage.
