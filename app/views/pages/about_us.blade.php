                        <p class="bm-remove">
                       Fast-track teams simply represent those that already have the knowledge required to build their product and thus only need supervision, learning resources and enabling environment. The duration of the learning program for this program is one month.                        </p>
                       
                        <p class="bm-remove">
The Scratch team represents groups that may have knowledge but not sufficient enough to build a working product. These group will be required to pass through a dash-training program spanning up to three weeks depending on how fast the team is able to learn. These group will utilize the Virtual Learning Platform for their training and as such will have to pay additionally for the training at a scholarship discount of 50%. This group will be required to undergo basic training within the first three weeks and processed normally as the Fast-track team after this 3 week learning period. </p>

