                                <h2>THE HUB</h2>
								<p>
									Given the vast positive changes going on in entrepreneurship globally, it is very inappropriate that youths with excellent dreams will attend the University in order to build their career and future but still end up helpless at the end of their programme. The goal of LionHUB thus is to bridge this gap, by teaching how to tap into these huge untapped potentials, become self-employed and create more jobs. Although LionHUB will not be able to cater for everyone, the general idea is to select the best of the best with great ideas and ensure that the harsh environment generally obtainable in Nigeria does not kill the dream of our promising youth.
								</p>
                                
                               <p class="bm-remove">
                              After the training stage, each team is meant to undergo a building phase before the final investor pitch. During the building phase, the coordinators will mentor each team in helping them understand the existing business and technological environment and getting in the right mindset to build great products. The team will be required to create objectives and milestones which will help track their daily progress and also learn from their mistakes.They are required to build towards this objective during the four week building stage after which they are expected to have produced a working prototype. At the end of this four weeks, they will submit their applications/products for testing and their applications are reviewed continually during the next two weeks. The investor pitch is expected to follow up a week after.
                                
                                </p>
                                
